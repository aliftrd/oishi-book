    private void btnBook_submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBook_submitMouseClicked
        if(!nomor_buku.getText().equals("") && !nama_buku.getText().equals("") && !harga_buku.getText().equals("")) {
            if(this.stock != 0) {
                tbl.addRow(new Object[] {
                    nomor_buku.getText(),
                    nama_buku.getText(),
                    harga_buku.getText()
                });
                this._setTotal();
                this.resetBook();
            } else {
                JOptionPane.showMessageDialog(this, "Stok buku ini sedang kosong!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong");
        }
    }//GEN-LAST:event_btnBook_submitMouseClicked

    private void trx_submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_trx_submitMouseClicked
        Vector dataTable = tbl.getDataVector();
        if(!text_anggota.getText().equals("") && !nama_anggota.getText().equals("") && dataTable.toArray().length != 0 && this.total_harga != 0) {
            String id_petugas = String.valueOf(Model.User.getId());
            String id_anggota = text_anggota.getText();
            String query;
            PreparedStatement ps;
            try {
                query = "INSERT INTO transaksi (id_transaksi, id_petugas, id_anggota, status, harga_total, tanggal_dibuat, tanggal_kembali, tanggal_dikembalikan) VALUES (NULL, '" + id_petugas + "', '" + id_anggota + "', 'Dipinjam', '" + this.total_harga + "', CURRENT_DATE(), DATE_ADD(CURRENT_DATE(), INTERVAL " + due_date.getSelectedItem() + " DAY), NULL)";
                Connection conn = (Connection)Database.GetConnection();
                try {
                    conn.setAutoCommit(false);
                    ps = conn.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
                    ps.executeUpdate();
                    ResultSet res = ps.getGeneratedKeys();
                    if(res.next()) {
                        this.currentTrxID = res.getInt(1);
                        Vector data = tbl.getDataVector();
                        for(int i = 0; i < data.toArray().length; i++) {
                            String id_buku = tbl.getValueAt(i, 0).toString();
                            query = "INSERT INTO detail_transaksi (id_transaksi, id_buku) VALUES ('" + this.currentTrxID + "', '" + id_buku + "')";
                            ps = conn.prepareStatement(query);
                            ps.execute();
                            
                            query = "UPDATE buku SET stock = IF(stock > 0, stock - 1, 0) WHERE id_buku = " + id_buku;
                            ps = conn.prepareStatement(query);
                            ps.execute();
                        }
                    }
                    conn.commit();
                    this.resetAll();
                    JOptionPane.showMessageDialog(this, "Berhasil menambahkan data");
                } catch(Exception ex) {
                    conn.rollback();
                    System.out.println(ex.getMessage());
                    JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menambahkan data");
                } finally {
                    conn.close();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan dengan database");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
        }
    }//GEN-LAST:event_trx_submitMouseClicked

