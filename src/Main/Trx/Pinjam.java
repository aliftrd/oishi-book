    protected  void _setTotal() {
        Vector data = tbl.getDataVector();
        int harga = 0;
        int lama_hari = Integer.valueOf(due_date.getSelectedItem().toString());
        for(int i = 0; i < data.toArray().length; i++) {
            int harga_per_buku = Integer.parseInt(cart_table.getValueAt(i, 2).toString());
            harga = harga + (harga_per_buku * lama_hari);
        }
        this.total_harga = harga;
        txt_total.setText(String.valueOf(this.total_harga));
    }
    
    protected  void resetBook() {
        nomor_buku.setText("");
        nama_buku.setText("");
        harga_buku.setText("");
    }
    
    protected  void resetAll() {
        this.resetBook();
        this.resetAnggota();
        this.total_harga = 0;
        txt_total.setText(String.valueOf(this.total_harga));
        tbl.setRowCount(0);
    }
    
    protected  void resetAnggota() {
        text_anggota.setText("");
        nama_anggota.setText("");
    }
