    private void btnSearch_pengarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearch_pengarangMouseClicked
        try {
            String query = "SELECT * FROM pengarang WHERE id_pengarang = '" + txt_pengarang.getText() + "'";
            Connection conn = (Connection)Database.GetConnection();
            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet result = ps.executeQuery();
            if(result.next()) {
                nama_pengarang.setText(result.getString("nama"));
            } else {
                nama_pengarang.setText("");
                JOptionPane.showMessageDialog(rootPane, "Maaf, pengarang tidak ditemukan silahkan periksa kembali!");
            }
            ps.close();
            conn.close();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnSearch_pengarangMouseClicked

