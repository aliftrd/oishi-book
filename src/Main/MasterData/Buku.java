    private void _datatables() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Nama");
        tbl.addColumn("ID Pengarang");
        tbl.addColumn("Pengarang");
        tbl.addColumn("ID Penerbit");
        tbl.addColumn("Penerbit");
        tbl.addColumn("Harga");
        tbl.addColumn("Jumlah Hal");
        tbl.addColumn("Stock");
        buku_table.setModel(tbl);
        try {
            String query = "SELECT * FROM buku JOIN penerbit ON buku.id_penerbit = penerbit.id_penerbit JOIN pengarang ON buku.id_pengarang = pengarang.id_pengarang ORDER BY id_buku DESC";
            Connection conn = (Connection)Database.GetConnection();
            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet result = ps.executeQuery();
            while(result.next()) {
                tbl.addRow(new Object[] {
                    result.getString("id_buku"),
                    result.getString("buku.nama"),
                    result.getString("pengarang.id_pengarang"),
                    result.getString("pengarang.nama"),
                    result.getString("penerbit.id_penerbit"),
                    result.getString("penerbit.nama"),
                    result.getString("buku.harga"),
                    result.getString("buku.jumlah_halaman"),
                    result.getString("buku.stock"),
                });
                buku_table.setModel(tbl);
            }
            ps.close();
            conn.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Terjadi kesalahan saat mengambil data");
        }
    }

